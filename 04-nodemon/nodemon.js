// Para instalar o nodemon, você pode usar o npm (Node Package Manager). Abra o terminal e execute o seguinte comando:

// npm install -g nodemon

// Isso instalará o nodemon globalmente em seu sistema, permitindo que você o use em qualquer projeto Node.js.

// Com o Nodemon instalado, você pode iniciar seu aplicativo Node.js usando o comando:

// nodemon nome-do-seu-arquivo.js

// Dessa forma, o Nodemon monitorará as alterações nos arquivos do seu projeto e reiniciará automaticamente o servidor sempre que você salvar uma modificação, tornando o desenvolvimento mais ágil e eficiente.

// Basta você salvar o arquivo e dar F5 no seu servidor, que o nodemon irá reiniciar automaticamente o servidor, sem a necessidade de você parar e iniciar manualmente o servidor a cada alteração.

// Para instalar como dependência de desenvolvimento em um projeto específico, você pode usar o seguinte comando:

// npm install --save-dev nodemon